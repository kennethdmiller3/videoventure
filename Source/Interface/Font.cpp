	texture.mEnvMode = GL_MODULATE;
	texture.mMinFilter = GL_NEAREST;
	texture.mMagFilter = GL_NEAREST;
	texture.mWrapS = GL_CLAMP;
	texture.mWrapT = GL_CLAMP;

